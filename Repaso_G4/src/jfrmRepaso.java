    private void jbtnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnAceptarActionPerformed
        String mensaje;
        mensaje = "El color seleccionado es: "+jltsColores.getSelectedValue();
        String color = jltsColores.getSelectedValue();
        
        switch(color){
            case "Rojo" -> {
                JOptionPane.showMessageDialog(this, mensaje);
                jPanelColor.setBackground(Color.red);
                jlblImagen.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/mario.png")));
            }
            case "Azul" -> {
                JOptionPane.showMessageDialog(this, mensaje);
                jPanelColor.setBackground(Color.blue);
                jlblImagen.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/sonic.png")));
            }
            case "Amarillo" -> {
                JOptionPane.showMessageDialog(this, mensaje);
                jPanelColor.setBackground(Color.yellow);
                jlblImagen.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/pikachu.png")));
            }
            case "Verde" -> {
                JOptionPane.showMessageDialog(this, mensaje);
                jPanelColor.setBackground(Color.green);
                jlblImagen.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/shrek.png")));
            }
            case "Negro" -> {
                JOptionPane.showMessageDialog(this, mensaje);
                jPanelColor.setBackground(Color.black);
                jlblImagen.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/amongus.png")));
            }
            case null -> JOptionPane.showMessageDialog(this, "Seleccione un color");
            default -> JOptionPane.showMessageDialog(this, "Seleccione un color Válido");
        }
    }//GEN-LAST:event_jbtnAceptarActionPerformed

